package nl.dead_pixel.telebot.api.types.files;

import com.fasterxml.jackson.annotation.JsonProperty;
import nl.dead_pixel.telebot.api.interfaces.IFile;


/**
 * The type Video note.
 */
public class VideoNote implements IFile {
    @JsonProperty("file_id")
    private String fileId;
    private Long length;
    private Long duration;
    private PhotoSize thumb;
    @JsonProperty("file_size")
    private Long fileSize;

    /**
     * Gets file id.
     *
     * @return the file id
     */
    public String getFileId() {
        return fileId;
    }

    /**
     * Sets file id.
     *
     * @param fileId the file id
     * @return the file id
     */
    private VideoNote setFileId(String fileId) {
        this.fileId = fileId;
        return this;
    }

    /**
     * Gets length.
     *
     * @return the length
     */
    public Long getLength() {
        return length;
    }

    /**
     * Sets length.
     *
     * @param length the length
     * @return the length
     */
    private VideoNote setLength(Long length) {
        this.length = length;
        return this;
    }

    /**
     * Gets duration.
     *
     * @return the duration
     */
    public Long getDuration() {
        return duration;
    }

    /**
     * Sets duration.
     *
     * @param duration the duration
     * @return the duration
     */
    private VideoNote setDuration(Long duration) {
        this.duration = duration;
        return this;
    }

    /**
     * Gets thumb.
     *
     * @return the thumb
     */
    public PhotoSize getThumb() {
        return thumb;
    }

    /**
     * Sets thumb.
     *
     * @param thumb the thumb
     * @return the thumb
     */
    private VideoNote setThumb(PhotoSize thumb) {
        this.thumb = thumb;
        return this;
    }

    /**
     * Gets file size.
     *
     * @return the file size
     */
    public Long getFileSize() {
        return fileSize;
    }

    /**
     * Sets file size.
     *
     * @param fileSize the file size
     * @return the file size
     */
    private VideoNote setFileSize(Long fileSize) {
        this.fileSize = fileSize;
        return this;
    }
}
